int	is_even(int nb)
{
	if (nb % 2 == 0)
		return (1);
	else
		return (0);
}

int	is_alpha(char c)
{
	if (c >= 'a' && c <= 'z')
		return (1);
	else if (c >= 'A' && c<= 'Z')
		return (1);
	else
		return (0);
}